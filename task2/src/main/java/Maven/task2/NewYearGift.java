package Maven.task2;

public class NewYearGift {
	private String name;
	private int quantity;
	
	public NewYearGift(String name,int quantity){
		this.name=name;
		this.quantity=quantity;
		
	}
	public String getName() {
		return name;
	}
	public int getQuantity() {
		return quantity;
	}
}
